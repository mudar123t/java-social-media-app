    private void hp_txt_postFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_hp_txt_postFocusLost
        if (hp_txt_post.getText().toLowerCase().equals("email")
            || hp_txt_post.getText().toLowerCase().equals("")) {
            hp_txt_post.setText("Whats on your minde?");
            hp_txt_post.setForeground(new Color(153, 153, 153));
        }
    }//GEN-LAST:event_hp_txt_postFocusLost

    private void hp_bAddPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hp_bAddPostActionPerformed
        String postText = hp_txt_post.getText();
        if (!postText.trim().equals("")) {
            Post npost = new Post();
            npost.iduser = Social_Media_Helper.getCurrentId();
            npost.text = postText;

            if (!postText.isEmpty()) {
                if (Social_Media_Helper.addPost(npost)) {
                    hp_txt_post.setText("");
                    hp_txt_area_posts.setText("");
                    ArrayList<String> user_posts = Social_Media_Helper.getUserPosts(Social_Media_Helper.getCurrentId());

                    for (String post : user_posts) {
                        //                        hp_txt_area_posts.append(Social_Media_Helper.getCurrentUser().name + "\n");
                        hp_txt_area_posts.append(post + "\n\n");
                        //                        postListModel.addElement(post);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Error adding post.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }

        } else {

        }
    }//GEN-LAST:event_hp_bAddPostActionPerformed

