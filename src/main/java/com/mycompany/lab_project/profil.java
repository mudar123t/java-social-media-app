    private void showUserFriendsDetails() {
        tbl_table_model.setRowCount(0);
        int currentUserId = this.userId;
        ArrayList<Integer> friendIds = Social_Media_Helper.getCurrentUserFriendsIds(currentUserId);
        for (int friendId : friendIds) {
            User friend = Social_Media_Helper.getCurrentUserFromId(friendId);
            if (friend != null) {
                tbl_table_model.addRow(new Object[]{friend.name, friend.lastname, friend.email, friend.gender, friend.nationality});
            }
        }
    }
