    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int selectedRow = profile_user_friends.getSelectedRow();
        if (selectedRow != -1) {
            String email = (String) tbl_table_model.getValueAt(selectedRow, 2);
            User selectedUser;
            selectedUser = Social_Media_Helper.getCurrentUserFromEmail(email);
            int friendId = selectedUser.iduser;
            if (Social_Media_Helper.deleteFriend(this.userId, friendId)) {
                JOptionPane.showMessageDialog(rootPane, "Friend deleted successfully");
                tbl_table_model.setRowCount(0);
                showUserFriendsDetails();

            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed to delete friend.");
            }
        } else {

            JOptionPane.showMessageDialog(rootPane, "No row selected.");
        }
    }//GEN-LAST:event_jButton4ActionPerformed

