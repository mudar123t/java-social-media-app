    private void editInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editInfoActionPerformed
        User user;
        user = Social_Media_Helper.getCurrentUserFromId(this.userId);
        String newName = JOptionPane.showInputDialog(this, "Enter updated name:", user.name);
        String newLastName = JOptionPane.showInputDialog(this, "Enter updated sure name:", user.lastname);
        String newPhoneNumber = JOptionPane.showInputDialog(this, "Enter updated name:", user.phonenumber);
        String newPassword = JOptionPane.showInputDialog(this, "Enter updated name:", user.password);
        if(Social_Media_Helper.editUserInfo(this.userId, newName, newLastName, newPhoneNumber, newPassword)){
            JOptionPane.showMessageDialog(rootPane,"Your Info has bee updated succsesfully","Info", JOptionPane.INFORMATION_MESSAGE);
            profile_name.setText(newName);
            profile_lastname.setText(newLastName);
        }else {
            JOptionPane.showMessageDialog(rootPane,"Error in updating","error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editInfoActionPerformed

