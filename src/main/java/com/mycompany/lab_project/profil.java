    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int selectedRow = profile_user_posts.getSelectedRow();
        if (selectedRow != -1) {
            int postId = (int) profile_user_posts.getModel().getValueAt(selectedRow, 0);
            String post = (String) profile_user_posts.getModel().getValueAt(selectedRow, 1);
            String editPost = JOptionPane.showInputDialog(rootPane,"Enter the new post you want", post);
            
            if (Social_Media_Helper.editPost(postId, editPost)) {
                profile_user_posts.setModel(new DefaultTableModel());
                DefaultTableModel user_posts = Social_Media_Helper.getCurrentUserPosts(this.userId);
                profile_user_posts.setModel(user_posts);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Failed to delete post", "warning", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No row selected.");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

