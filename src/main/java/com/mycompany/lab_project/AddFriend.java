    private void addFriend_bAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addFriend_bAddActionPerformed
        int selectedRow = addFriend_tbl.getSelectedRow();
        if (selectedRow != -1) {
            String email = (String) tbl_table_model.getValueAt(selectedRow, 2);
            User selectedUser;
            selectedUser = Social_Media_Helper.getCurrentUserFromEmail(email);
            int friendId = selectedUser.iduser; 
            int currentUserId = Social_Media_Helper.getCurrentId();
            if (Social_Media_Helper.addFriend(currentUserId, friendId)) {
                JOptionPane.showMessageDialog(rootPane, "Friend added successfully.");
                tbl_table_model.setRowCount(0);
                showSuggestions(tbl_table_model);
            } else {
                JOptionPane.showMessageDialog(rootPane,"Failed to add friend.");
            }
        } else {
            
            JOptionPane.showMessageDialog(rootPane, "No row selected.");
        }
    }//GEN-LAST:event_addFriend_bAddActionPerformed

