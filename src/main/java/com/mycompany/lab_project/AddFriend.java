    public static boolean showSuggestions(DefaultTableModel tbl_table_model) {
        
        tbl_table_model.setRowCount(0);
        int currentUserId = Social_Media_Helper.getCurrentId();
        ArrayList<Integer> friendIds = Social_Media_Helper.getCurrentUserFriendsIds(currentUserId);
        
        try {
            conn = DriverManager.getConnection(connectionString);
            Statement stmt = conn.createStatement();
            String query = "SELECT iduser, name, lastname, email, gender, nationality FROM socialmedia.user";
            rs = stmt.executeQuery(query);

            tbl_table_model.setRowCount(0);
            while (rs.next()) {
                int iduser = rs.getInt("iduser");
                String name = rs.getString("name");
                String lastname = rs.getString("lastname");
                String email = rs.getString("email");
                String gender = rs.getString("gender");
                String nationality = rs.getString("nationality");

                if (iduser != Social_Media_Helper.getCurrentId() &&  !friendIds.contains(iduser)) {
                    tbl_table_model.addRow(new Object[]{name, lastname, email, gender, nationality});
                } else {

                }
            }

            return true;
        } catch (SQLException ex) {
            System.out.println("Error fetching data: " + ex.getMessage());
            return false;
        } finally {
            if (conn != null) {
                try {
                    conn.close();
                } catch (SQLException ex) {
                    Logger.getLogger(Social_Media_Helper.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }
